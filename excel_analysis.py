import pandas as pd

# ---- 读取 Excel ----
df = pd.read_excel("学员线索.xlsx")

# ---- 1. 看一眼数据长什么样 ----
print("===== 全部数据 =====")
print(df)

print(f"\n共 {len(df)} 条线索")

# ---- 2. 基本统计 ----
print("\n===== 成绩统计 =====")
print(f"平均分：{df['当前成绩'].mean():.1f}")
print(f"最高分：{df['当前成绩'].max()}")
print(f"最低分：{df['当前成绩'].min()}")

# ---- 3. 筛选：成绩低于 70 的（重点跟进对象）----
print("\n===== 成绩 < 70（重点跟进）=====")
low_score = df[df["当前成绩"] < 70]
print(low_score[["姓名", "当前成绩", "科目", "咨询次数"]])

# ---- 4. 筛选：咨询次数 >= 3 且未付费（高意向客户）----
print("\n===== 高意向客户（咨询≥3次 + 未付费）=====")
hot_leads = df[(df["咨询次数"] >= 3) & (df["是否付费"] == "否")]
print(hot_leads[["姓名", "咨询次数", "当前成绩", "科目"]])